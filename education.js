let podcastProgressInterval = null;

function togglePodcast() {
    if (currentAudio && !currentAudio.paused) {
        currentAudio.pause();
        setAudioPlaying(false);
        clearInterval(podcastProgressInterval);
        return;
    }
    
    if (currentAudio && currentAudio.paused && currentAudio.currentTime > 0) {
        currentAudio.play();
        setAudioPlaying(true);
        startPodcastProgress();
        return;
    }
    
    stopAudio();
    currentAudio = new Audio(eduState.podcastSrc);
    
    currentAudio.onplay = () => {
        setAudioPlaying(true);
        startPodcastProgress();
    };
    currentAudio.onpause = () => {
        if (currentAudio && currentAudio.ended) return;
        setAudioPlaying(false);
        clearInterval(podcastProgressInterval);
    };
    currentAudio.onended = () => {
        setAudioPlaying(false);
        clearInterval(podcastProgressInterval);
        updatePodcastProgress();
    };
    currentAudio.onerror = () => {
        setAudioPlaying(false);
        const status = document.getElementById('audio-status');
        if (status) status.textContent = 'Аудио недоступно — квест можно завершить';
        document.getElementById('edu-complete-btn').disabled = false;
    };
    
    currentAudio.play();
}

function startPodcastProgress() {
    clearInterval(podcastProgressInterval);
    podcastProgressInterval = setInterval(updatePodcastProgress, 500);
}

function updatePodcastProgress() {
    if (!currentAudio) return;
    const dur = currentAudio.duration || 0;
    const cur = currentAudio.currentTime || 0;
    if (dur <= 0) return;
    
    const pct = cur / dur;
    const fill = document.getElementById('podcast-progress-fill');
    if (fill) fill.style.width = (pct * 100) + '%';
    
    const curEl = document.getElementById('podcast-current-time');
    const totEl = document.getElementById('podcast-total-time');
    if (curEl) curEl.textContent = formatTime(cur);
    if (totEl) totEl.textContent = formatTime(dur);
    
    // Enable complete at 80%
    if (pct >= 0.8 && !eduState.podcastReady) {
        eduState.podcastReady = true;
        document.getElementById('edu-complete-btn').disabled = false;
        const status = document.getElementById('audio-status');
        if (status) status.textContent = 'Можно завершить квест!';
    }
}

function formatTime(sec) {
    const m = Math.floor(sec / 60);
    const s = Math.floor(sec % 60);
    return m + ':' + (s < 10 ? '0' : '') + s;
}

function podcastSeek(delta) {
    if (currentAudio) {
        currentAudio.currentTime = Math.max(0, Math.min(currentAudio.duration || 0, currentAudio.currentTime + delta));
        updatePodcastProgress();
    }
}


// ===== Lesson Renderer =====
function renderLesson(quest, container) {
    const sections = quest.content.sections;
    eduState.lessonSection = 0;
    
    container.innerHTML = `
        <div class="lesson-container">
            <div class="lesson-progress">
                <span id="lesson-counter">Раздел 1 из ${sections.length}</span>
                <div class="fc-progress-bar"><div id="lesson-progress-fill" style="width: ${100/sections.length}%"></div></div>
            </div>
            <div class="lesson-content" id="lesson-content">
                <h3 class="lesson-section-title">${sections[0].title}</h3>
                <div class="lesson-section-text">${sections[0].text}</div>
                ${sections[0].wowFact ? '<div class="info-funfact"><span class="ff-icon">💡</span><p><strong>Интересный факт:</strong> ' + sections[0].wowFact + '</p></div>' : ''}
            </div>
            <div class="flashcard-nav">
                <button class="btn btn-sm" id="lesson-prev" onclick="lessonNav(-1)" disabled>← Назад</button>
                <button class="btn btn-sm btn-glow" id="lesson-next" onclick="lessonNav(1)">Далее →</button>
            </div>
        </div>
    `;
}

function lessonNav(dir) {
    const quest = EDU_QUESTS.find(q => q.id === currentQuestId);
    if (!quest || quest.type !== 'lesson') return;
    
    const sections = quest.content.sections;
    const newIdx = eduState.lessonSection + dir;
    if (newIdx < 0 || newIdx >= sections.length) return;
    
    eduState.lessonSection = newIdx;
    const s = sections[newIdx];
    
    const content = document.getElementById('lesson-content');
    content.innerHTML = `
        <h3 class="lesson-section-title">${s.title}</h3>
        <div class="lesson-section-text">${s.text}</div>
        ${s.wowFact ? '<div class="info-funfact"><span class="ff-icon">💡</span><p><strong>Интересный факт:</strong> ' + s.wowFact + '</p></div>' : ''}
    `;
    
    document.getElementById('lesson-counter').textContent = `Раздел ${newIdx + 1} из ${sections.length}`;
    document.getElementById('lesson-progress-fill').style.width = ((newIdx + 1) / sections.length * 100) + '%';
    document.getElementById('lesson-prev').disabled = (newIdx === 0);
    
    const nextBtn = document.getElementById('lesson-next');
    if (newIdx === sections.length - 1) {
        nextBtn.textContent = '✓ Готово';
        nextBtn.onclick = () => {
            document.getElementById('edu-complete-btn').disabled = false;
        };
    } else {
        nextBtn.textContent = 'Далее →';
        nextBtn.onclick = () => lessonNav(1);
    }
}

// ===== Complete Education Quest =====
function completeEduQuest(questId) {
    if (state.completed.includes(questId)) return;

    const quest = EDU_QUESTS.find(q => q.id === questId);
    if (!quest) return;

    state.history.unshift({
        questId: quest.id,
        questName: quest.name,
        questIcon: quest.icon,
        points: quest.points,
        comment: `Образовательный квест: ${quest.type}`,
        date: new Date().toISOString(),
        status: 'approved',
        isEdu: true,
    });

    state.completed.push(quest.id);
    state.completedQuests++;
    state.points += quest.points;
    state.xp += quest.points;

    while (state.xp >= state.xpNext) {
        state.xp -= state.xpNext;
        state.level++;
        state.xpNext = Math.floor(state.xpNext * 1.5);
    }

    const today = new Date().toDateString();
    if (state.lastTrainDate) {
        const last = new Date(state.lastTrainDate);
        const diff = Math.floor((new Date(today) - last) / 86400000);
        if (diff === 1) state.streak++;
        else if (diff > 1) state.streak = 1;
    } else {
        state.streak = 1;
    }
    state.lastTrainDate = today;

    // Check achievements
    let newAchievement = null;
    ACHIEVEMENTS.forEach(ach => {
        if (!state.unlockedAchievements.includes(ach.id) && ach.condition(state)) {
            state.unlockedAchievements.push(ach.id);
            newAchievement = ach;
        }
    });

    saveState();
    logEvent(currentUserId, 'edu_quest_complete', { questId: questId });
    stopAudio();

    document.getElementById('success-text').textContent = `Квест «${quest.name}» выполнен!`;
    document.getElementById('success-points').textContent = `+${quest.points}`;

    const achEl = document.getElementById('success-achievement');
    if (newAchievement) {
        achEl.hidden = false;
        achEl.textContent = `Новое достижение: ${newAchievement.icon} ${newAchievement.name}`;
    } else {
        achEl.hidden = true;
    }

    document.getElementById('success-modal').hidden = false;
}

